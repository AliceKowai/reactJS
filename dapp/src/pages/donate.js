import Head from "next/head";
import Link from "next/link";
import Footer from "@/components/Footer";
import { useState } from "react";
import { getCampaign, donate } from "../services/Web3Service";

export default function Donate() {

    const [campaign, setCampaign] = useState({});
    const [donation, setDonation] = useState(0);
    const [message, setMessage] = useState("");

    function onChangeId(evt) {
        campaign.id = evt.target.value;
    }

    function btnSearchClick() {
        setMessage("Buscando...aguarde...");
        getCampaign(campaign.id)
            .then(result => {
                setMessage("");
                result.id = campaign.id;
                setCampaign(result);
            })
            .catch(err => setMessage(err.message));
    }

    function onChangeValue(evt) {
        setDonation(evt.target.value);
    }

    function btnDonateClick() {
        setMessage("Doando...aguarde...");
        donate(campaign.id, donation)
            .then(tx => setMessage(`Doação realizada, obrigado. Em alguns minutos o saldo será atualizado.`))
            .catch(err => setMessage(err.message));
    }

    return (
        <>
            <Head>
                <title>Donate Crypto | Fazer Doação</title>
                <meta charSet='utf-8' />
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
            </Head>
            <div className="container">
                <h1 className="text-6xl p-4 max-w-md">Donate Crypto</h1>
                {
                    !campaign.id
                        ? (
                            <>
                                <p className="mx-5">
                                    Qual é o ID da campanha que procura?
                                </p>
                                <div>
                                    <div className="flex w-96">
                                        <input type="number" id="campaignId" className="w-full px-4 py-2.5 text-base text-coolGray-900 font-normal oultine-none focus:border-green-500 border  ml-5 mb-10 boder-coolGray-200 shadown-input mt-4" onChange={onChangeId} value={campaign.id} />
                                        <input type="button" value="Buscar" className="py-2 mt-4 px-5 text-center text-white font-bold text-sm leading-6 bg-blue-500 rounded-r-2xl transition duration-200 mb-10" onClick={btnSearchClick} />
                                    </div>
                                </div>
                            </>
                        )
                        : (
                            <>
                                <p className="mx-5">Verifique se esta é a campanha certa antes de finalizar sua doação.</p>
                                <hr />
                                <div className="flex">
                                    <div className="px-3">
                                        {
                                            campaign.videoUrl
                                                ? <iframe width="100%" height="480" src={campaign.videoUrl}></iframe>
                                                : <img src={campaign.imageUrl} className="mx-10" width="640" height="480" />
                                        }
                                    </div>
                                    <div className="mx-10" style={{ height: 480, scrollbars: true }}>
                                        <h2>{campaign.title}</h2>
                                        <p><strong>Autor:</strong>{campaign.author}</p>
                                        <p className="mb-3">{campaign.description}</p>
                                        {
                                            campaign.videoUrl
                                                ? <p>Assista o vídeo ao lado para entender mais sobre nossa campanha.</p>
                                                : <></>
                                        }
                                        <p className="mb-3 fst-italic mt-5">
                                            E aí, o que achou do projeto? Já foi arrecadado {campaign.balance / 10 ** 18} BNB nesta campanha. O quanto você quer doar (em BNB)?
                                        </p>
                                        <div className="mb-3">
                                            <div className="input-group">
                                                <input type="number" id="donation" className="px-4 py-2.5 text-base text-coolGray-900 font-normal oultine-none focus:border-green-500 border  ml-5 mb-10 boder-coolGray-200 shadown-input mt-4" onChange={onChangeValue} value={donation} />
                                                <input type="button" value="Doar" className="py-2 mt-4 px-5 text-center text-white font-bold text-sm leading-6 bg-blue-500 rounded-r-2xl transition duration-200 mb-10" onClick={btnDonateClick} />
                                                <span className="input-group-text"> BNB</span>
                                               
                                            </div>
                                        </div>
                                    </div>
                                </div>
                            </>
                        )
                }
                {
                    message
                        ? <div className="alert alert-success p-3 col-6" role="alert">{message}</div>
                        : <></>
                }
                <Footer />
            </div>
        </>
    )
}